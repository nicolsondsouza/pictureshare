serverURL = "http://192.168.1.101:3000"
app.upload = function (imageURI) {
    try{
    var ft = new FileTransfer(),
        options = new FileUploadOptions();

    options.fileKey = "file";
    options.fileName = imageURI.substr(imageURI.lastIndexOf('/')+1);; // We will use the name auto-generated by Node at the server side.
    options.mimeType = "image/jpeg";
    options.chunkedMode = false;
    options.params = { // Whatever you populate options.params with, will be available in req.body at the server-side.
        "description": "Uploaded from my phone"
    };

    ft.upload(imageURI, serverURL + "/images",
        function (e) {
            alert("success");
        },
        function (e) {
            alert("Upload failed");
        }, options);
    }
    catch(err){
        alert(err);
    }
}